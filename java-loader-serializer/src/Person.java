import java.util.Date;

public class Person {
  public String name, lastName, phone;
  public Date date;

  // generated
  @Override
  public String toString() {
    return "Person{" +
      "name='" + name + '\'' +
      ", lastName='" + lastName + '\'' +
      ", phone='" + phone + '\'' +
      ", date=" + date +
      '}';
  }

}
